#! /bin/bash

DB_NAME=sales.db

rm -f ${DB_NAME}
sqlite3 ${DB_NAME} << EOF
CREATE TABLE sales(
  "ORDERNUMBER" INTEGER,
  "QUANTITYORDERED" NUMERIC,
  "PRICEEACH" NUMERIC,
  "ORDERLINENUMBER" INTEGER,
  "SALES" TEXT,
  "ORDERDATE" TEXT,
  "STATUS" TEXT,
  "QTR_ID" TEXT,
  "MONTH_ID" TEXT,
  "YEAR_ID" TEXT,
  "PRODUCTLINE" TEXT,
  "MSRP" TEXT,
  "PRODUCTCODE" TEXT,
  "CUSTOMERNAME" TEXT,
  "PHONE" TEXT,
  "ADDRESSLINE1" TEXT,
  "ADDRESSLINE2" TEXT,
  "CITY" TEXT,
  "STATE" TEXT,
  "POSTALCODE" TEXT,
  "COUNTRY" TEXT,
  "TERRITORY" TEXT,
  "CONTACTLASTNAME" TEXT,
  "CONTACTFIRSTNAME" TEXT,
  "DEALSIZE" TEXT
);

.mode csv
.import sales_data_sample.csv sales

EOF
